       $(document).ready(function() {

            $(".create_order").click(function(e){
            e.preventDefault();

         
            var BASE_URL = "{{ url('/') }}";
            var _token = $("input[name='_token']").val();
            var client_source = $('#client_source').val();
            var client_name = $('#client_name').val();
            var order_name = $('#order_name').val();
            var type_of_data = $('#type_of_data').val();
            var number_of_image = $('#number_of_image').val();
            var order_type =$('#order_type').val();
            var client_edited =$('#client_edited').val();
            var number_of_edit=$('#number_of_edit').val();        


          
            $.ajax({
                url: BASE_URL+'/store_order_data',
                type:'POST',
                data: {_token:_token,client_source:client_source,client_name:client_name,order_name:order_name,number_of_image:number_of_image,order_type:order_type,client_edited:client_edited,number_of_edit:number_of_edit,type_of_data:type_of_data},
                success: function(data) {
    
                    if($.isEmptyObject(data.error)){

                        $( '.orederform' ).each(function(){

                             this.reset();
                        
                        });  
                                  
                         $('.error-text').text('');

                         $('#addOrderModalside').modal('hide');

   
                     createAlert('','Success!','Order data inserted successfully!!.','success',true,true,'pageMessages');

                    $('.order_view').html("");
                    $('.order_view').html(data);

                    }else{
                        printErrorMsg(data.error);
                    }
                 }
               });
             }); 

               function printErrorMsg (msg) {

               $('.co_login label').css("margin", "10px auto 0");

               $.each( msg, function( key, value ) {
               console.log(key);
              $('.'+key+'_error').text(value);
            });
         }
    });



